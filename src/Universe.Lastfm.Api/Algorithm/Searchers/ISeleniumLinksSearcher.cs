using System.Collections.Generic;
using OpenQA.Selenium;

namespace Universe.Lastfm.Api.Algorithm.Searchers
{
	/// <summary>
	///		Получает ссылки для поиска критериев на странице через Selenium
	/// </summary>
	public interface ISeleniumLinksSearcher
	{
        /// <summary>
		///     Получает ссылки HREF
		///		Игнорирует ошибки WebDriverException
		/// </summary>
		/// <returns></returns>
		List<LinkInformation> GetPageSearchLinksSafe(IWebDriver driver, string sessionId);

        /// <summary>
        ///     Получает ссылки IMG
        ///		Игнорирует ошибки WebDriverException
        /// </summary>
        /// <returns></returns>
        List<LinkImageInformation> GetPageSearchImgLinksSafe(IWebDriver driver, string sessionId);
    }
}
