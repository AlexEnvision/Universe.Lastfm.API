using System;
using System.Collections.Generic;
using System.Diagnostics.CodeAnalysis;
using Universe.Lastfm.Api.Algorithm.Searchers;

namespace Universe.Lastfm.Api.Algorithm.Comparers
{
    public class LinkInformationUriComparer<TLinkInformation> : IEqualityComparer<TLinkInformation>
        where TLinkInformation : LinkInformation

    {
        public bool Equals(TLinkInformation x, TLinkInformation y)
        {
            var xUrl = x?.Url;
            var yUrl = y?.Url;
            if (xUrl == null && yUrl == null)
                return false;

            return Uri.Compare(xUrl, yUrl, UriComponents.HttpRequestUrl, UriFormat.SafeUnescaped,
                StringComparison.OrdinalIgnoreCase) == 0;
        }

        public int GetHashCode([DisallowNull] TLinkInformation obj)
        {
            return obj.Url.GetHashCode();
        }
    }
}