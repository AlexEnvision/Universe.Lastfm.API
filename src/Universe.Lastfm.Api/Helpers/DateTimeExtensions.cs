using System;

namespace Universe.Lastfm.Api.Helpers
{
    /// <summary>
    ///     Contains extensions are relating and associating with <see cref="DateTime"/>
    /// </summary>
    public static class DateTimeExtensions
    {
        /// <summary>
        ///     Transform unixTimeStamp to DateTime
        /// </summary>
        /// <param name="unixTimeStamp">The time stamp</param>
        /// <returns>Datetime from the time stamp</returns>
        public static DateTime UnixTimeStampToDateTime(this double unixTimeStamp)
        {
            // Unix timestamp is seconds past epoch
            DateTime dateTime = new DateTime(1970, 1, 1, 0, 0, 0, 0, DateTimeKind.Utc);
            dateTime = dateTime.AddSeconds(unixTimeStamp).ToLocalTime();
            return dateTime;
        }

        /// <summary>
        ///     Transform unixTimeStamp to DateTime
        /// </summary>
        /// <param name="unixTimeStamp">The time stamp</param>
        /// <returns>Datetime from the time stamp</returns>
        public static DateTime UnixTimeStampToDateTime(this long unixTimeStamp)
        {
            // Unix timestamp is seconds past epoch
            DateTime dateTime = new DateTime(1970, 1, 1, 0, 0, 0, 0, DateTimeKind.Utc);
            dateTime = dateTime.AddSeconds(unixTimeStamp).ToLocalTime();
            return dateTime;
        }
    }
}
