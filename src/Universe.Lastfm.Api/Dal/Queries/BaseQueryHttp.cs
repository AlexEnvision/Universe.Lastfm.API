using System;
using System.Collections.Generic;
using System.Net.Http;
using Newtonsoft.Json;

namespace Universe.Lastfm.Api.Dal.Queries
{
    public class BaseQueryHttp : BaseQuery
    {
        protected T GetResult<T>(string method, KeyValuePair<string, string>[] parameters)
        {
            using (var client = new HttpClient())
            {
                var uri = $"{Root.IntegretedServerHost}/{method}";

                var builder = new UriBuilder(uri);

                builder.Query = new FormUrlEncodedContent(parameters).ReadAsStringAsync().Result;

                var stringUri = builder.ToString();

                string response;

                try
                {
                    response = new HttpClient().GetStringAsync(stringUri).Result;
                }
                catch (Exception ex)
                {
                    throw;
                }

                var result = JsonConvert.DeserializeObject<T>(response);

                return result;
            }
        }
    }
}
